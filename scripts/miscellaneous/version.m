## -*- texinfo -*-
## @deftypefn {Function File} {} version ()
## Return Octave's version number as a string.  This is also the value of
## the built-in variable @code{OCTAVE_VERSION}.
## @end deftypefn

## Author: jwe

function vs = version ()

  if (nargin != 0)
    warning ("version: ignoring extra arguments");
  endif

  vs = OCTAVE_VERSION;

endfunction
